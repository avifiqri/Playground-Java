package com.playground;

public class Main {
    public static void main(String[] args) {

        //operator aritmatika

        // tambah ( + )
        float angka1 = 3;
        float angka2 = 4;

        System.out.println(angka1 + angka2);

        System.out.println("///////////////////////////////////////");

        // kurang ( - )
        System.out.println(angka1 - angka2);

        System.out.println("///////////////////////////////////////");

        // kali ( * )
        System.out.println(angka1 * angka2);

        System.out.println("///////////////////////////////////////");

        //bagi ( / )
        System.out.print(angka1 / angka2);


    }
}
